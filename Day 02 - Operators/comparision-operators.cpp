#include <iostream>
using namespace std;
#define endl '\n'

int main()
{
  cout << "Comparision Operators" << endl;
  int a = 10, b = 20;

  cout << (a < b) << endl;
  cout << (a > b) << endl;
  cout << (a == b) << endl;
  cout << (a >= b) << endl;
  cout << (a <= b) << endl;
  cout << (a != b) << endl;
  return 0;
}

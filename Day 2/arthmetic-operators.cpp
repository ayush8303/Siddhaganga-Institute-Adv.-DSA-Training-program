#include <iostream>
using namespace std;
#define endl '\n'

int main()
{
  cout << "Arthmetic Operators" << endl;
  int a = 10, b = 20;
  // cin>>a>>b;
  cout << (a + b) << endl;
  cout << (a - b) << endl;
  cout << (a * b) << endl;
  cout << (a / b) << endl; // floor division
  cout << (a % b) << endl; // remainder of a/b
  cout << "A " << a << endl;
  cout << "B " << b << endl;
  cout << (-10) % 3 << endl;
  return 0;
}
